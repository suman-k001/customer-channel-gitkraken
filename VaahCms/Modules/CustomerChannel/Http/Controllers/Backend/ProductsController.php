    public function postActions(Request $request, $action) : JsonResponse
    {
        try {
            $rules = array(
                'inputs' => 'required',
            );
            $validator = \Validator::make( $request->all(), $rules);
            if ( $validator->fails() ) {

                $errors             = errorsToArray($validator->errors());
                $response['success'] = false;
                $response['errors'][] = $errors;
                return response()->json($response);
            }

            $response = [];
            $request->merge(['action'=>$action]);
            switch ($action)
            {
                //------------------------------------
                case 'toggle-user-active-status':

                    $response = Product::changeUserStatus($request);

                    break;
                //------------------------------------
                case 'toggle-all-user-active-status':

                    $response = Product::bulkChangeUserStatus($request);

                    break;
                //------------------------------------
            }
        } catch (\Exception $e) {
            $response = [];
            $response['success'] = false;

            if(env('APP_DEBUG')){
                $response['errors'][] = $e->getMessage();
                $response['hint'][] = $e->getTrace();
            } else {
                $response['errors'][] = 'Something went wrong.';
            }
        }

        return response()->json($response);
    }
